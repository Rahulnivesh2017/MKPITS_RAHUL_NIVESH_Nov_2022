// Even and odd number

import java.util.*;
class Odd_Even_java
{
 public static void main(String []args)
 {
   int n;
   System.out.println("Enter any number");
   Scanner sc=new Scanner(System.in);
   n=sc.nextInt();
   if(n%2==0)
   {
    System.out.print("Even number");
    }
    else
    {
    System.out.print("Odd number");
    }
}
}

